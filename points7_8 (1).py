"""ERFNET"""

import os
import cv2
import glob
import torch
import random
from PIL import Image
import numpy as np
from erfnet import ERFNet
from argparse import ArgumentParser
from sklearn.metrics import average_precision_score, roc_curve
from torchvision.transforms import Compose, Resize, ToTensor

# Reproducibility
seed = 42
random.seed(seed)
np.random.seed(seed)
torch.manual_seed(seed)

n_classes = 20

# Improving GPU performance
torch.backends.cudnn.deterministic = True
torch.backends.cudnn.benchmark = True

# Image transformations:
input_transform = Compose([Resize((512, 1024), Image.BILINEAR), ToTensor()]) #Bilinear for RGB
target_transform = Compose([Resize((512, 1024), Image.NEAREST)]) #NN for discrete masks


# Metrics
def fpr_at_95_tpr(confidences, labels):
    """
    Calculates the False Positive Rate (FPR) when the
    True Positive Rate (TPR) is at exactly 95%.
    """
    fpr, tpr, thresholds = roc_curve(labels, confidences)
    idx = np.argmax(tpr >= 0.95)
    return fpr[idx]


def main():
    parser = ArgumentParser()
    parser.add_argument("--input", required=True, help="Glob pattern for input images.")

    parser.add_argument('--loadDir', default="/content/MaskArchitectureAnomaly_CourseProject/trained_models/")
    parser.add_argument('--loadWeights', default="erfnet_pretrained.pth")
    parser.add_argument('--method', default="maxlogit", choices=["maxlogit", "msp", "entropy"])
    args = parser.parse_args()

    # Initialization
    model = ERFNet(n_classes)
    model = torch.nn.DataParallel(model).cuda()

    weightspath = os.path.join(args.loadDir, args.loadWeights)

    # To handle models saved with or without DataParallel
    def load_my_state_dict(model, state_dict):
        state = model.state_dict()
        for name, param in state_dict.items():
            if name not in state:
                if name.startswith("module."):
                    state[name.split("module.")[-1]].copy_(param)
            else:
                state[name].copy_(param)
        return model

    # Load weights into the model and set to evaluation mode
    model = load_my_state_dict(model, torch.load(weightspath, map_location=lambda storage, loc: storage))
    model.eval()

    anomaly_score_list = []
    ood_gts_list = []


    # Scoring Loop
    for path in glob.glob(os.path.expanduser(args.input)):
        # Load transform and move the image to the GPU
        images = input_transform((Image.open(path).convert('RGB'))).unsqueeze(0).float().cuda()

        # Ensure channel dimension is correct
        images = images.permute(0,3,1,2) if images.shape[1] != 3 else images

        # Forward pass without calculating gradients
        with torch.no_grad():
            result = model(images)

        # Extract raw logits and move back to CPU
        logits = result.squeeze(0).cpu().numpy()

        # Calculate anomaly scores based on the chosen method
        if args.method == "maxlogit":
            # 1.0 minus the maximum logit across all classes
            anomaly_result = 1.0 - np.max(logits, axis=0)
        elif args.method == "msp":
            # Maximum Softmax Probability: 1.0 minus the highest confidence probability
            probs = torch.softmax(torch.from_numpy(logits), dim=0).numpy()
            anomaly_result = 1.0 - np.max(probs, axis=0)
        elif args.method == "entropy":
            # Normalized Predictive Entropy
            probs = torch.softmax(torch.from_numpy(logits), dim=0).numpy()
            entropy = -np.sum(probs * np.log(probs + 1e-10), axis=0)
            anomaly_result = entropy / np.log(n_classes)


        # Dynamically find the corresponding mask file based on the image path
        pathGT = path.replace("images", "labels_masks")
        if "RoadObsticle21" in pathGT: pathGT = pathGT.replace("webp", "png")
        if "fs_static" in pathGT: pathGT = pathGT.replace("jpg", "png")
        if "RoadAnomaly" in pathGT: pathGT = pathGT.replace("jpg", "png")

        mask = target_transform(Image.open(pathGT))
        ood_gts = np.array(mask)

        # Standardize mask values across different datasets:
        # 0 = Normal, 1 = Anomaly, 255 = Ignore
        if "RoadAnomaly" in pathGT:
            ood_gts = np.where((ood_gts==2), 1, ood_gts)

        if "LostAndFound" in pathGT:
            ood_gts = np.where((ood_gts==0), 255, ood_gts) # Ignore background
            ood_gts = np.where((ood_gts==1), 0, ood_gts)   # Normal
            ood_gts = np.where((ood_gts>1)&(ood_gts<201), 1, ood_gts) # Anomalies

        # Skip images that do not contain any actual anomalies (class 1)
        if 1 not in np.unique(ood_gts): continue

        # Store results for final metric calculation
        ood_gts_list.append(ood_gts)
        anomaly_score_list.append(anomaly_result)

        # Free up GPU memory
        torch.cuda.empty_cache()

    #Final Metric Calculation

    # Abort if no valid ground truth images were processed
    if len(ood_gts_list) == 0:
        return

    # Convert lists to numpy arrays
    ood_gts = np.array(ood_gts_list)
    anomaly_scores = np.array(anomaly_score_list)

    # Create boolean masks to separate normal (ind) and anomalous (ood) pixels
    ood_mask = (ood_gts == 1)
    ind_mask = (ood_gts == 0)

    # Flatten and concatenate the scores and labels into 1D arrays
    val_out = np.concatenate((anomaly_scores[ind_mask], anomaly_scores[ood_mask]))
    val_label = np.concatenate((np.zeros(np.sum(ind_mask)), np.ones(np.sum(ood_mask))))

    # Calculate the area under the PR Curve and FPR at 95% TPR
    prc_auc = average_precision_score(val_label, val_out)
    fpr = fpr_at_95_tpr(val_out, val_label)

    print(f'AUPRC score: {prc_auc*100.0:.2f}%')
    print(f'FPR@TPR95: {fpr*100.0:.2f}%')

if __name__ == '__main__':
    main()

