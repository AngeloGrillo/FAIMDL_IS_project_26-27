"""Temperature Scaling"""

import os
import glob
import torch
import numpy as np
from PIL import Image
from argparse import ArgumentParser
from erfnet import ERFNet
from sklearn.metrics import average_precision_score, roc_curve
from torchvision.transforms import Compose, Resize, ToTensor


n_classes = 20

# Bilinear interpolation for the RGB images, Nearest Neighbor for the integer masks
input_transform = Compose([Resize((512, 1024), Image.BILINEAR), ToTensor()])
target_transform = Compose([Resize((512, 1024), Image.NEAREST)])

def fpr_at_95_tpr(confidences, labels):
    """Calculates the False Positive Rate when the True Positive Rate hits 95%."""
    fpr, tpr, _ = roc_curve(labels, confidences)
    idx = np.argmax(tpr >= 0.95)
    return fpr[idx]

def main():

    parser = ArgumentParser()
    parser.add_argument("--input", required=True, help="Glob pattern for input images.")


    parser.add_argument('--loadWeights', default="./trained_models/erfnet_pretrained.pth")
    args = parser.parse_args()

    model = ERFNet(n_classes)
    model = torch.nn.DataParallel(model).cuda()

    state_dict = torch.load(args.loadWeights, map_location='cpu', weights_only=True)
    own_state = model.state_dict()
    for name, param in state_dict.items():
        if name not in own_state:
            if name.startswith("module."):
                own_state[name.split("module.")[-1]].copy_(param)
        else:
            own_state[name].copy_(param)

    # Set model to evaluation mode
    model.eval()

    # Setup
    # Define the temperatures to test. T=1.0 is the standard baseline.
    temperatures = [0.5, 0.75, 1.0, 1.1, 1.5, 2.0]

    # Dictionary to hold the anomaly scores for every temperature setting
    anomaly_scores = {t: [] for t in temperatures}
    ood_gts_list = []


    for path in glob.glob(os.path.expanduser(args.input)):
        # Load and transform image
        images = input_transform((Image.open(path).convert('RGB'))).unsqueeze(0).float().cuda()
        images = images.permute(0,3,1,2) if images.shape[1] != 3 else images

        with torch.no_grad():
            result = model(images)

        # Extract raw unnormalized logits
        logits = result.squeeze(0)

        # Apply temperature scaling: Divide logits by T before calculating Softmax
        for t in temperatures:
            scaled_logits = logits / t
            probs = torch.softmax(scaled_logits, dim=0).cpu().numpy()

            # Anomaly score is 1.0 minus the highest confidence class probability
            anomaly_score = 1.0 - np.max(probs, axis=0)
            anomaly_scores[t].append(anomaly_score)

        # Processing
        pathGT = path.replace("images", "labels_masks")
        if "RoadObsticle21" in pathGT: pathGT = pathGT.replace("webp", "png")
        if "fs_static" in pathGT: pathGT = pathGT.replace("jpg", "png")
        if "RoadAnomaly" in pathGT: pathGT = pathGT.replace("jpg", "png")

        mask = target_transform(Image.open(pathGT))
        ood_gts = np.array(mask)

        # Standardize labels: 0 = Normal, 1 = Anomaly, 255 = Ignore
        if "RoadAnomaly" in pathGT:
            ood_gts = np.where((ood_gts==2), 1, ood_gts)

        if "LostAndFound" in pathGT:
            ood_gts = np.where((ood_gts==0), 255, ood_gts)
            ood_gts = np.where((ood_gts==1), 0, ood_gts)
            ood_gts = np.where((ood_gts>1)&(ood_gts<201), 1, ood_gts)

        # If the image has no valid anomalies, remove the appended scores for this image across all temperatures to keep arrays aligned
        if 1 not in np.unique(ood_gts):
            for t in temperatures:
                anomaly_scores[t].pop()
            continue

        ood_gts_list.append(ood_gts)
        torch.cuda.empty_cache()

    if len(ood_gts_list) == 0:
        print("No valid Ground Truth found for this dataset.")
        return

    ood_gts_arr = np.array(ood_gts_list)
    ood_mask = (ood_gts_arr == 1)
    ind_mask = (ood_gts_arr == 0)

    # Create the flattened true label array
    val_label = np.concatenate((np.zeros(np.sum(ind_mask)), np.ones(np.sum(ood_mask))))

    # Evaluate metrics for each temperature setting independently
    for t in temperatures:
        scores_arr = np.array(anomaly_scores[t])

        # Flatten predictions
        val_out = np.concatenate((scores_arr[ind_mask], scores_arr[ood_mask]))

        prc_auc = average_precision_score(val_label, val_out)
        fpr = fpr_at_95_tpr(val_out, val_label)

        print(f"T={t:<4} | AUPRC: {prc_auc*100.0:.2f}% | FPR@TPR95: {fpr*100.0:.2f}%")

if __name__ == '__main__':
    main()

